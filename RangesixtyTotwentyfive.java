/*Write a java program to print the numbers from 60-25*/
//creating the class range
public class RangesixtyTotwentyfive {
	public static void main(String[] args) {
//		Initialize the integer type variable to store the value 
		int i=60;
//		creating the while loop check condition weather start from 60 and decrement to 25 
		while (i>=25) {
//			print the checked number
			System.out.println(i);
//			decrement
			i--;
		}
	}
}